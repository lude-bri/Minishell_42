#include "../includes/minishell.h"

void	msh_env(char **envp)
{
	int		i;
	int		j;

	i = 0;
	while (envp[i])
	{
		j = 0;
		while (envp[i][j])
		{
			write(1, &envp[i][j], 1);
			j++;
		}
		write (1, "\n", 1);
		i++;
	}
}
